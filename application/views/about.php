<h4>Original Game Rules (1937)</h4>


The game has six stocks, which in fact are commodities. These six are gold, silver,
Bonds, oil, industrials, and grain. During gameplay all the stocks are identical. Each
stock begins costing a dollar apiece. Players are given starting money of $5000 and they
buy shares in groups of 500, 1000, 2000, or 5000. The stocks move based on the throw
of three dice. The first die picks the stock that will be affected, with one of the
commodities on each side of the die. The second die determines what whether the stock
will move up, down, or pay a dividend. The third die decides if the movement or dividend
will be five, ten, or twenty cents. For instance a roll of Industrials, Down, 20 will move the
industrials stock from its start value of $1.00 to 80 cents. A roll of Grain, Up, 5 would
move grain up to $1.05.
Dividends are paid out only for any stocks that are at or above $1.00 in value. For
instance, a five cent dividend pays five cents for each share owned. Thus if a roll is Oil,
Dividend, 10, and you own a thousand shares of Oil at $1.25 apiece you will receive a
dividend of $100. Note that the dividend is not affected by the value of the share, with
only the rule being that stocks worth less than $1.00 do not pay out when a dividend is
rolled for them.
If a stock ever reaches $2.00 it splits. Everyone who owns the stock doubles the number
of sharers owned and the stock goes back to being worth $1.00. If a stock falls to being
worth nothing all players lose their investment in that stock and must return their shares
to the bank. The stock is then reset at $1.00.

There are two basic strategies to making money at the game. The first is to buy
whichever stocks are safely in dividend paying territory. Using this method if a stock
splits, and returns to the $1.00 value the owner should sell out and reinvest in a stock
that is safely above par. Similarly if a stock drops below a dollar the owner should sell
the shares. This technique relies upon the rule that any money invested in a stock
paying dividends will earn a greater return than shares that are not paying dividends.
The second strategy is more risky, but can also be immensely profitable. It involves
buying stocks when they are near the bottom of the board and at risk of being worth
nothing. Since the dice rolling system moves stocks by a fixed amount rather than a
percentage of their value these stocks are very volatile. One thousand dollars will buy
ten thousand shares of a ten cent stock. While this stock could move down by ten or
twenty and be wiped out the most one would lose is a thousand dollars. However there
is an equal chance that the stock will move up, and a single roll of Up 20 will triple the
original investment. The possible return on investing in a five cent stock, the cheapest
possible, is even higher.

