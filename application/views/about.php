<h4>Stock Ticker History</h4>
The game is based on Stock Ticker, a now out of print board game that was
popular upon its release and is still played today. It was released by Copp-Clark
Publishing, a venerable Canadian publisher, in 1937. The original rules are
shown here, by student request.
This document is not part of any COMP4711 assignment, but presents some
interesting background material about the game our assignments are based on.
